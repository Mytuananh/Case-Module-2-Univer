package model.teachers;

public abstract class GeneralInformationTeacher {
    abstract String getName();
    abstract void setName(String name);
    abstract String getDateOfBirth();
    abstract void setDateOfBirth(String dateOfBirth);
    abstract String getCodeTeacher();
    abstract void setCodeTeacher(String codeTeacher);
    abstract String getHomeTown();
    abstract void setHomeTown(String homeTown);
}
