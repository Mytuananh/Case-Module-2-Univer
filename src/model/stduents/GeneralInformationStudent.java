package model.stduents;

public abstract class GeneralInformationStudent {
    abstract String getName();
    abstract void setName(String name);
    abstract String getDateOfBirth();
    abstract void setDateOfBirth(String dateOfBirth);
    abstract String getCodeStudent();
    abstract void setCodeStudent(String codeStudent);
    abstract String getClassName();
    abstract void setClassName(String className);
    abstract String getHomeTown();
    abstract void setHomeTown(String homeTown);
}
